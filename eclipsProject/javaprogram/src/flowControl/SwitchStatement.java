/*Switch Statement  is a multipul chosice dision making select statement 
 *  if usese whwen we went to select  only one case out of multipul cases
 *  syntax  switch(ex)
 *  {
 *  case1(Statement)
 *  break;
 *  case2(Statement)
 *  break;
 *  }*/
public class SwitchStatement {
	public static void main(String [] args) {
		int a= 10;
		System.out.println("i am switch Statement");
		switch(a){
		case 1:
		System.out.println(1);
		break;
		case 2:
    	System.out.println(2);
    	break;
		default:
		System.out.println("defult");
			
		}
	}
	
	

}
